import numpy as np
from pydantic import BaseModel, ValidationError, validator


class UserModel(BaseModel):
    name: str
    username: str
    password1: str
    password2: str

    @validator('name')
    def name_must_contain_space(cls, v):
        if ' ' not in v:
            raise ValueError('must contain a space')
        return v.title()

    @validator('username')
    def username_validation(cls, v):
        a = eval(v)
        return np.array(a)


a = UserModel(name="abc de", username="[[1, 2], [3, 4]]", password1="a", password2="v")
print(a.username)